-- ── cmo_drafts ─────────────────────────────────────────────────────────────
-- Freeform tweet drafts generated by M (Claude API via the
-- generate-tweet-copy Edge Function). Each row holds:
--   prompt           the freeform request from admin ("draft a launch tweet")
--   variations       jsonb [{copy, hashtags, rationale}, ...]
--   selected_index   int · which variation admin picked
--   status           draft → approved → posted → archived
CREATE TABLE IF NOT EXISTS public.cmo_drafts (
  id             uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  prompt         text NOT NULL,
  variations     jsonb NOT NULL,
  selected_index int,
  status         text NOT NULL DEFAULT 'draft' CHECK (status IN ('draft', 'approved', 'posted', 'archived')),
  notes          text,
  created_at     timestamptz NOT NULL DEFAULT now(),
  created_by     uuid REFERENCES public.members(id) ON DELETE SET NULL,
  updated_at     timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_cmo_drafts_created_at ON public.cmo_drafts (created_at DESC);
CREATE INDEX IF NOT EXISTS idx_cmo_drafts_status     ON public.cmo_drafts (status, created_at DESC);

ALTER TABLE public.cmo_drafts ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "admins read cmo_drafts"  ON public.cmo_drafts;
DROP POLICY IF EXISTS "admins write cmo_drafts" ON public.cmo_drafts;
CREATE POLICY "admins read cmo_drafts" ON public.cmo_drafts FOR SELECT
  USING (EXISTS (SELECT 1 FROM public.members WHERE id = auth.uid() AND is_admin));
CREATE POLICY "admins write cmo_drafts" ON public.cmo_drafts FOR ALL
  USING (EXISTS (SELECT 1 FROM public.members WHERE id = auth.uid() AND is_admin))
  WITH CHECK (EXISTS (SELECT 1 FROM public.members WHERE id = auth.uid() AND is_admin));

GRANT SELECT, INSERT, UPDATE, DELETE ON public.cmo_drafts TO authenticated;
GRANT ALL ON public.cmo_drafts TO service_role;

-- Auto-update updated_at on UPDATE.
CREATE OR REPLACE FUNCTION public.cmo_set_updated_at()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN NEW.updated_at = now(); RETURN NEW; END;
$$;

DROP TRIGGER IF EXISTS trg_cmo_templates_updated_at ON public.cmo_templates;
CREATE TRIGGER trg_cmo_templates_updated_at BEFORE UPDATE ON public.cmo_templates
  FOR EACH ROW EXECUTE FUNCTION public.cmo_set_updated_at();

DROP TRIGGER IF EXISTS trg_cmo_drafts_updated_at ON public.cmo_drafts;
CREATE TRIGGER trg_cmo_drafts_updated_at BEFORE UPDATE ON public.cmo_drafts
  FOR EACH ROW EXECUTE FUNCTION public.cmo_set_updated_at();
