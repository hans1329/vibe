-- CMO Post Studio · runtime storage for editable trigger templates +
-- freeform tweet drafts generated via Claude. Admin-only at the RLS
-- layer; UI lives at /admin/cmo (CmoPreviewPage).

-- ── cmo_templates ────────────────────────────────────────────────────────
-- 5 trigger types with editable tweet copy templates. id is the
-- canonical trigger key (audit_complete · graduation · milestone ·
-- weekly_picks · early_spotter). The frontend keeps SVG share-card
-- design hardcoded in the React component for now; only the tweet
-- COPY field is admin-editable from this table. SVG slot editing
-- arrives in a follow-up.
CREATE TABLE IF NOT EXISTS public.cmo_templates (
  id            text PRIMARY KEY,
  label         text NOT NULL,
  copy_template text NOT NULL,
  fires_when    text NOT NULL,
  data_source   text NOT NULL,
  updated_at    timestamptz NOT NULL DEFAULT now(),
  updated_by    uuid REFERENCES public.members(id) ON DELETE SET NULL
);

ALTER TABLE public.cmo_templates ENABLE ROW LEVEL SECURITY;

-- Admin-only · the template surface is internal marketing config, not
-- a public-facing concept like rulebook copy.
DROP POLICY IF EXISTS "admins read cmo_templates"  ON public.cmo_templates;
DROP POLICY IF EXISTS "admins write cmo_templates" ON public.cmo_templates;
CREATE POLICY "admins read cmo_templates" ON public.cmo_templates FOR SELECT
  USING (EXISTS (SELECT 1 FROM public.members WHERE id = auth.uid() AND is_admin));
CREATE POLICY "admins write cmo_templates" ON public.cmo_templates FOR ALL
  USING (EXISTS (SELECT 1 FROM public.members WHERE id = auth.uid() AND is_admin))
  WITH CHECK (EXISTS (SELECT 1 FROM public.members WHERE id = auth.uid() AND is_admin));

GRANT SELECT, INSERT, UPDATE, DELETE ON public.cmo_templates TO authenticated;
GRANT ALL ON public.cmo_templates TO service_role;

